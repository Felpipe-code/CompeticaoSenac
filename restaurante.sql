CREATE TABLE Frequentadores (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    numero INT,
    datanascimento DATE,
    cpf VARCHAR(14) UNIQUE
);

CREATE TABLE login (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    id_funcionario BIGINT,
    nome VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    cpf VARCHAR(14) UNIQUE,
    senha VARCHAR(255) NOT NULL,
    cargo ENUM('Admin', 'Funcionario')
);

CREATE TABLE Agendamento (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(255),
    numero INT,
    horadia TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    mesa INT,
    horadiaagendado TIMESTAMP NULL,
    id_empregado BIGINT,
    id_cliente BIGINT,
    FOREIGN KEY (id_empregado) REFERENCES login(id),
    FOREIGN KEY (id_cliente) REFERENCES Frequentadores(id)
);

CREATE TABLE Pedidos (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    Garçom VARCHAR(255),
    mesa INT,
    Numeropedido INT,
    Bebida ENUM('Suco', 'Refrigerante', 'Água', 'Cerveja', 'Vinho'),
    iniciopedido TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    terminopedido TIMESTAMP NULL,
    idcliente BIGINT,
    FOREIGN KEY (idcliente) REFERENCES Frequentadores(id)
);


